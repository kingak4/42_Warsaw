#include <stdio.h>
#include <unistd.h>

void pyn(int n)
{
	if (n >= 10)
		pyn(n / 10);
	write(1,&"0123456789"[n % 10],1);
}

int main(int ac, char *av[])
{
	pyn(ac);
	write(1,&ac,1);
}